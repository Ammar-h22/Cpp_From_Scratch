#include <iostream>
using namespace std;

int main()
{
    int num1, num2;
    cout << "Enter the value of num1: \n";
    cin >> num1;
    cout << "Enter the value of num2: \n";
    cin >> num2;

    cout << "The sum of num1 and num2 is: " << num1 + num2;
    // cout << num1 + num2 << " is the sum of num1 and num2\n";

    return 0;
}
// (<<)  ---> This is called as insertion operator.
// (>>)  ---> This is called as extraction operator.
